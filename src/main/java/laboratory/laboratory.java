package laboratory;

public class laboratory {
	
	private String id;
    private String name;
    private String phone;
	private String email;
	private String date;
	private String address;
	private String test;
	
	public laboratory(String id, String name, String phone, String email, String date, String address, String test) {
		
		this.id = id;
		this.name = name;
		this.phone = phone;
		this.email = email;
		this.date = date;
		this.address = address;
		this.test = test;
	}

	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}

	public String getPhone() {
		return phone;
	}

	public String getEmail() {
		return email;
	}

	public String getDate() {
		return date;
	}

	public String getAddress() {
		return address;
	}

	public String getTest() {
		return test;
	}

	
	
	
	
	
	
	
	
	
}	
	
	